package comprog12.prelim.printpattern;

public class PrintStarPattern {

	public static void main(String[] args) {
		System.out.println("    *    ");
		System.out.println("* *   * *");
		System.out.println("   * *   ");
		System.out.println("  *   *  ");
		System.out.println(" *     * ");
		System.out.println("   (d)   ");
	}

}
