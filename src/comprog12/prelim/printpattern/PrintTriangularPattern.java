package comprog12.prelim.printpattern;

public class PrintTriangularPattern {

	public static void main(String[] args) {
		System.out.println("* * * * *");
		System.out.println(" *     * ");
		System.out.println("  *   *  ");
		System.out.println("   * *   ");
		System.out.println("    *    ");
		System.out.println("   (c)   ");
	}

}
