package comprog12.prelim.arithmetic;

public class SumProduct3Numbers {     // Save as "SumProduct3Numbers.java"
	public static void main(String[] args) {
		int number1 = 98; // Declare an int variable number1 and initialize it to 98
		int number2 = 5;  // Declare an int variable number2 and initialize it to 5
		int number3 = 77; // Declare an int variable number3 and initialize it to 77

		// Compute the sum and product of all three numbers
		int sum = number1 + number2 + number3;
		int product = number1 * number2 * number3;

		// Print the sum and product
		System.out.println("The sum of all three numbers is: " + sum);
		System.out.println("The product of all three numbers is: " + product);
	}
}