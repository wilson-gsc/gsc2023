package comprog12.prelim.intsum;

public class SixIntegerSum {   // Save as "SixIntegerSum.java"
	public static void main(String[] args) {
		int number1 = 11;  // Declare 6 integer variables and assign values
		int number2 = 22;
		int number3 = 33;
		int number4 = 44;
		int number5 = 55;
		int number6 = 66;
		int sum;  // Declare an integer variable called sum to hold the sum
		sum = number1 + number2 + number3 + number4 + number5 + number6;  // Compute sum
		System.out.print("The sum is ");  // Print a descriptive string
		// Cursor stays after the printed string
		System.out.println(sum);  // Print the value stored in variable sum
		// Cursor advances to the beginning of next line
	}
}